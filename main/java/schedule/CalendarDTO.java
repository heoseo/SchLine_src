package schedule;

public class CalendarDTO {

	private String month;
	private String year;
	
	//getter/setter 메소드 생성.
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	
}
