package schline;

public class UserSettingDTO {
	
	private String usersetting_idx;
	private String user_id;
	private String setting_name;
	private String user_set;
	
	public String getUsersetting_idx() {
		return usersetting_idx;
	}
	public void setUsersetting_idx(String usersetting_idx) {
		this.usersetting_idx = usersetting_idx;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getSetting_name() {
		return setting_name;
	}
	public void setSetting_name(String setting_name) {
		this.setting_name = setting_name;
	}
	public String getUser_set() {
		return user_set;
	}
	public void setUser_set(String user_set) {
		this.user_set = user_set;
	}
	
	
}
